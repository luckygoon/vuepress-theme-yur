<template>
  <div id="yur-footer">
    <div class="footer-wrap">
      <a-row>
        <a-col
          :xs="24"
          :sm="24"
          :md="8"
        >
          <div class="footer-center">
            <h2>
              <a-skeleton
                active
                :loading="loading"
              >
